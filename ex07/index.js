// ex07: function to add 2 numbers

// declaration of add function
function add(n1 , n2)
{
	var sum = n1 + n2;
	return sum;
}

// calling the function
console.log(add(5,6));

// bonus

// declaration of product function
function prod(m1 , m2)
{
	var mul = m1 * m2;
	return mul;
}

// calling the function
result = prod(5,6);
console.log(result);
